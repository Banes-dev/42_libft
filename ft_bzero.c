#include "libft.h"

void	*ft_bzero(void *b, size_t len)
{
	size_t	i;

	if (!b)
		return (NULL);
	i = 0;
	while (i < len)
	{
		*(char *)(b + i) = 0;
		i++;
	}
	return (b);
}
